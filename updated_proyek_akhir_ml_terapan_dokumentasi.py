"""**TF-IDF Vectorizer dan Cosine Similarity**

Pada bagian ini, kita membangun model Content-Based Filtering dengan pendekatan berbasis teks menggunakan TF-IDF (Term Frequency-Inverse Document Frequency). Langkah-langkahnya:

- **TF-IDF Vectorizer** digunakan untuk mengubah teks dari kolom `combined` (gabungan fitur seperti judul, genre, dan deskripsi) menjadi representasi numerik (vektor).
- `stop_words='english'` membantu menghilangkan kata-kata umum (seperti "the", "and", dll) agar hasil lebih akurat.
- **Cosine Similarity** digunakan untuk menghitung kemiripan antara setiap drama berdasarkan vektor TF-IDF-nya.
- Mapping dari nama drama ke indeks dibuat menggunakan `pd.Series`, sehingga sistem dapat dengan cepat mencari posisi drama tertentu berdasarkan judulnya.

Langkah ini menjadi dasar untuk proses pencarian dan pemberian rekomendasi berdasarkan kemiripan konten antar drama.
"""

# TF-IDF Vectorizer
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.metrics.pairwise import cosine_similarity

tfidf = TfidfVectorizer(stop_words='english')
tfidf_matrix = tfidf.fit_transform(drama_content['combined'])

# Cosine similarity
cosine_sim = cosine_similarity(tfidf_matrix, tfidf_matrix)

# Index mapping (judul -> index baris)
indices = pd.Series(drama_content.index, index=drama_content['drama_name'].str.lower())

"""**Mendapatkan Rekomendasi**

Fungsi `recommend(query, num_recommendations=5)` bertujuan untuk memberikan rekomendasi drama berdasarkan input pengguna, baik melalui judul drama maupun nama aktor. Berikut adalah penjelasan alur logikanya:

#### 1. Input Judul Drama
- Query (judul drama) diubah menjadi lowercase untuk mencocokkan dengan indeks (`indices`).
- Jika judul ditemukan:
  - Sistem mengambil indeks drama tersebut dari dataset.
  - Menghitung **cosine similarity** antara drama tersebut dan semua drama lain.
  - Drama yang sama (query itu sendiri) dihapus dari hasil.
  - Rekomendasi diurutkan berdasarkan skor kemiripan tertinggi.
  - Mengembalikan `num_recommendations` drama teratas beserta informasi: `drama_name`, `rating`, dan `actor_name`.

#### 2. Fallback: Jika Judul Tidak Ditemukan, Coba Nama Aktor
- Jika query tidak ditemukan sebagai judul:
  - Sistem mencoba mencocokkan query dengan nama aktor (`actor_name`).
  - Jika cocok:
    - Sistem mengambil drama lain dari aktor tersebut (selain yang sudah pernah ditampilkan) dan menampilkannya.
  - Jika tidak ditemukan juga, maka akan ditampilkan pesan:
    > "Judul drama atau nama aktor tidak ditemukan."

Fungsi ini fleksibel, karena mampu menangani input berupa nama drama atau nama aktor, dan memberikan rekomendasi alternatif jika data tidak ditemukan secara langsung.
"""

def recommend(query, num_recommendations=5):
    query = query.lower()

    idx = indices.get(query)

    if idx is not None:
        # Hitung similarity
        sim_scores = list(enumerate(cosine_sim[idx]))

        # Buang diri sendiri dari hasil rekomendasi
        sim_scores = [s for s in sim_scores if s[0] != idx]

        # Urutkan berdasarkan similarity
        sim_scores = sorted(sim_scores, key=lambda x: x[1], reverse=True)

        # Ambil indeks drama teratas
        drama_indices = [i[0] for i in sim_scores[:num_recommendations]]

        return drama_content[['drama_name', 'rating', 'actor_name']].iloc[drama_indices]

    else:
        # Coba cari berdasarkan nama aktor
        actor_dramas = drama_content[drama_content['actor_name'].str.lower() == query]

        if actor_dramas.empty:
            return "Judul drama atau nama aktor tidak ditemukan."

        seen_titles = set(actor_dramas['drama_name'])
        other_dramas = drama_content[
            (drama_content['actor_name'].str.lower() == query) &
            (~drama_content['drama_name'].isin(seen_titles))
        ]

        return other_dramas[['drama_name', 'rating', 'actor_name']].head(num_recommendations)

