"""**Evaluasi**

Evaluasi dilakukan untuk mengukur seberapa relevan hasil rekomendasi yang diberikan oleh sistem. Salah satu metrik evaluasi yang digunakan adalah **Precision@K**, yang mengukur proporsi item relevan di antara K rekomendasi teratas.

#### Fungsi-fungsi yang digunakan:

- `get_relevant_items_by_rating()`:  
  Mengembalikan daftar nama drama dengan rating di atas ambang batas (`rating > 7`) sebagai item relevan.

- `recommend()`:  
  Memberikan rekomendasi drama berdasarkan judul input. Fungsi ini mengambil indeks drama dari query, menghitung cosine similarity, lalu mengembalikan nama-nama drama yang paling mirip.

- `precision_at_k()`:  
  Menghitung Precision@K berdasarkan jumlah item relevan yang muncul dalam `k` rekomendasi teratas. Jika tidak ada rekomendasi atau item relevan, maka precision dikembalikan sebagai `0.0`.

#### Contoh Evaluasi:

```python
query = "Crash Landing on You"
k = 3

recommended = recommend(query, k)
actual = get_relevant_items_by_rating(drama_content)

precision = precision_at_k(recommended, actual, k)
print(f"Precision@{k}: {precision:.2f}")
"""

def get_relevant_items_by_rating(df, rating_threshold=7):
    return df[df['rating'] > rating_threshold]['drama_name'].str.lower().tolist()

def recommend(query, num_recommendations=5):
    query = query.lower()
    if query not in indices:
        return []
    idx = indices[query]
    sim_scores = list(enumerate(cosine_sim[idx]))
    sim_scores = sorted(sim_scores, key=lambda x: x[1], reverse=True)[1:num_recommendations+1]
    drama_indices = [i[0] for i in sim_scores]
    return drama_content['drama_name'].iloc[drama_indices].str.lower().tolist()

def precision_at_k(recommended, actual, k):
    if not recommended or not actual:
        return 0.0
    return len([item for item in recommended[:k] if item in actual]) / k

# Input user
query = "Crash Landing on You"
k = 3

# Dapatkan rekomendasi dan item relevan
recommended_items = recommend(query, k)
relevant_items = get_relevant_items_by_rating(drama_content)

# Evaluasi
if recommended_items:
    precision = precision_at_k(recommended_items, relevant_items, k)
    print("Recommended items:", recommended_items)
    print("Relevant items:", relevant_items)
    print(f"Precision@{k}: {precision:.2f}")
else:
    print(f"Drama '{query}' tidak ditemukan dalam database.")

